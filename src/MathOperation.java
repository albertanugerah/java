public class MathOperation {
    public static void main(String[] args) {
        int a = 12;
        int b = 3;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

//        augmented assignments
        int c = 100;
        c+=10;
        System.out.println(c);

        c-=10;
        System.out.println(c);

        c*=10;
        System.out.println(c);

//        Unary Operator
        int d = 10;
        d++;
        System.out.println(d);


    }
}
